package operators;

public class IncrementDecrementOperators {

	public static void main(String[] args) {
		int a=10;
		int b=20;
		System.out.println(a);
		System.out.println(b);
		
		System.out.println(a++);
		
		System.out.println(b--);
				
		int c=30;
		int d=40;
        System.out.println(++c);
        
		System.out.println(--d);
	}

}
